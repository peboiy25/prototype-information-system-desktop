    public class FMasukTableModel extends AbstractTableModel{
        
        private List<DetailFMasuk> listDetailFMasuk = new ArrayList<>();
        private final String HEADER[] = {"Kode Barang", "Warna", "Berat", "Jumlah"};
        
        public FMasukTableModel (List<DetailFMasuk> listDetailFMasuk) {
            this.listDetailFMasuk = listDetailFMasuk;
        }
        
        @Override
        public int getRowCount() {
            return listDetailFMasuk.size();
        }

        @Override
        public int getColumnCount() {
            return HEADER.length;
        }

        @Override
        public String getColumnName(int i) {
            return HEADER[i];
        }
        
        @Override
        public Object getValueAt(int i, int i1) {
            DetailFMasuk detail = listDetailFMasuk.get(i);
            switch(i1){
                case 0:
                    return detail.getBarang().getKodeBarang();
                case 1:
                    return detail.getWarna();
                case 2:
                    return detail.getBerat();
                case 3:
                    return detail.getJumlah();
                default:
                    return "";
            }
        }
        
    }
