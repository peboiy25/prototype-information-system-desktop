    private void tombolSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolSimpanActionPerformed
        // TODO add your handling code here:
        fMasuk.setNoFaktur(textNoFMasuk.getText());
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        fMasuk.setTanggal(sdf.format(tanggalFMasuk.getDate()));
        fMasuk.setNamaPabrik(textNamaPabrik.getText());
        fMasuk.setStokMasuk(Integer.parseInt(textTotal.getText()));
        App.fakturService.simpanFakturMasuk(fMasuk);
        
        for (DetailFMasuk detailFMasuk : listDetailFMasuk) {
            DetailFMasuk dfm = new DetailFMasuk();
            dfm.setfMasuk(fMasuk);
            dfm.setBarang(detailFMasuk.getBarang());
            dfm.setWarna(detailFMasuk.getWarna());
            dfm.setBerat(detailFMasuk.getBerat());
            dfm.setJumlah(detailFMasuk.getJumlah());
            App.fakturService.simpanDetailFMasuk(dfm);
        }
        fMasukController.clearForm();
        listDetailFMasuk = new ArrayList<>();
        refreshTable();
        JOptionPane.showMessageDialog(this, "Faktur Masuk berhasil disimpan!");
    }//GEN-LAST:event_tombolSimpanActionPerformed

