    public class BarangTableModel extends AbstractTableModel {

        private List<Barang> listBarang = new ArrayList<>();

        private final String HEADER[] = {"Kode Barang", "Nama Barang", "Satuan", "Harga", "Grand Stok"};

        public BarangTableModel(List<Barang> listBarang) {
            this.listBarang = listBarang;
        }

        @Override
        public int getRowCount() { // jumlah baris
            return listBarang.size();
        }

        @Override
        public int getColumnCount() { // jumlah kolom
            return HEADER.length;
        }

        @Override
        public String getColumnName(int i) { // nama kolom
            return HEADER[i];
        }

        @Override
        public Object getValueAt(int i, int i1) { // mengisi data
            Barang b = listBarang.get(i); // baris
            switch (i1) { // kolom
                case 0:
                    return b.getKodeBarang();
                case 1:
                    return b.getNamaBarang();
                case 2:
                    return b.getSatuan();
                case 3:
                    return b.getHarga();
                case 4:
                    return b.getGrandStok();
                default:
                    return null;
            }
        }
    }
