    private void tombolSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolSimpanActionPerformed
        // TODO add your handling code here:
        if (barangController.validasiInput()) {
            barang.setKodeBarang(textKode.getText());
            barang.setNamaBarang(textNama.getText());
            barang.setSatuan(textSatuan.getText());
            barang.setHarga(Integer.parseInt(textHarga.getText()));
            barang.setGrandStok(Double.parseDouble(textGrand.getText()));

            App.masterService.simpanBarang(barang);
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan !", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            refreshTable();
        }
    }//GEN-LAST:event_tombolSimpanActionPerformed

    private void tombolHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolHapusActionPerformed
        // TODO add your handling code here:
        if (barangController.validasiInput()) {
            barang.setKodeBarang(textKode.getText());
            int konfirmasi = JOptionPane.showConfirmDialog(this, "Apakah anda yakin akan menghapus data ini?", "Konfirmasi", JOptionPane.WARNING_MESSAGE);
            if(konfirmasi==0){
                App.masterService.hapusBarang(barang);
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus !", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                refreshTable();
            }
        }
    }//GEN-LAST:event_tombolHapusActionPerformed

