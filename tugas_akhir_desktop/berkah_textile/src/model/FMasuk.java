package model;

/**
 *
 * @author LENOVO
 */
public class FMasuk {
    private String noFaktur;
    private String tanggal;
    private Integer stokMasuk;
    private String namaPabrik;

    public String getNoFaktur() {
        return noFaktur;
    }

    public void setNoFaktur(String noFaktur) {
        this.noFaktur = noFaktur;
    }

    public String getTanggal() {
        return tanggal;
    }

    public void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }

    public Integer getStokMasuk() {
        return stokMasuk;
    }

    public void setStokMasuk(Integer stokMasuk) {
        this.stokMasuk = stokMasuk;
    }

    public String getNamaPabrik() {
        return namaPabrik;
    }

    public void setNamaPabrik(String namaPabrik) {
        this.namaPabrik = namaPabrik;
    }
    
    
}
